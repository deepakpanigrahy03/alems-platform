#!/usr/bin/env python3
"""
================================================================================
DATABASE MANAGER – Module 4: Persistent Storage for A‑LEMS Experiments
================================================================================

PURPOSE:
    This module provides a complete SQLite database interface for storing
    all experiment data generated by the A‑LEMS system. It implements the
    final 10‑table schema designed for research‑grade analysis, including
    runs, orchestration events, high‑frequency samples, tax summaries,
    and a flattened ML view.

WHY IT EXISTS:
    - To enable long‑term storage and reproducibility of all measurements.
    - To support GUI queries (fast, indexed) for interactive exploration.
    - To provide a single flat table (ml_features) for machine learning training.
    - To preserve all raw and derived data for future analysis without
      having to re‑run experiments.

UNITS AND SCIENTIFIC ASSUMPTIONS:
    - All times are stored in nanoseconds (ns) for integer precision.
    - All energies are stored in microjoules (µJ) to avoid floating‑point drift.
    - Power is stored in watts (J/s) as REAL.
    - Frequencies in MHz (megahertz), temperatures in °C.
    - Baseline correction: dynamic_energy = total_energy - idle_baseline_energy.
    - Orchestration tax = agentic_dynamic - linear_dynamic (paired runs).
    - Tax is distributed proportionally to each event's energy.
    - All derived columns (energy_per_instruction, etc.) are computed in Python
      to ensure correctness and are stored for ML convenience.
    - We assume that linear and agentic runs are paired by `run_number` within
      the same experiment.

WHAT IT MUST NEVER BREAK:
    - Must never lose data: all inserts are transactional.
    - Must never corrupt foreign key relationships.
    - Must never insert duplicate runs (enforced by unique index).
    - Must never miscalculate tax due to missing pairs.

AUTHOR: Deepak Panigrahy
================================================================================
"""


class DatabaseManager:
    """
    Main interface to the A‑LEMS SQLite database.

    This class handles all interactions with the persistent storage:
    - Creating tables and indexes.
    - Inserting experiments, runs, events, samples.
    - Computing tax summaries and run state hashes.
    - Providing query methods for GUI and ML.

    All insert operations are transactional to ensure consistency.
    Units are strictly enforced: times in ns, energies in µJ.
    """

    def __init__(self, db_path: str = "data/experiments.db"):
        """
        Initialize database connection and create tables if they don't exist.

        Args:
            db_path: Path to the SQLite database file.
                    Default is "data/experiments.db" relative to project root.
        """
        # Ensure the directory exists – SQLite won't create intermediate directories.
        Path(db_path).parent.mkdir(parents=True, exist_ok=True)

        self.db_path = db_path
        # Connect and enable foreign key support (critical for data integrity).
        self.conn = sqlite3.connect(db_path)
        self.conn.execute("PRAGMA foreign_keys = ON")
        # Use Row factory so we can access columns by name.
        self.conn.row_factory = sqlite3.Row

        # Create all tables and indexes.
        self._create_tables()

        logger.info(f"DatabaseManager initialized with {db_path}")

    def _create_tables(self):
        """
        Create all tables and indexes defined in the schema.

        This method is called once during initialization.
        It uses IF NOT EXISTS so subsequent calls are safe.
        """
        with self.conn:
            # Execute each CREATE statement in order (foreign keys must exist before references).
            self.conn.execute(CREATE_EXPERIMENTS)
            self.conn.execute(CREATE_HARDWARE_CONFIG)
            self.conn.execute(CREATE_IDLE_BASELINES)
            self.conn.execute(CREATE_RUNS)
            self.conn.executescript(CREATE_RUNS_INDEXES)
            self.conn.execute(CREATE_ORCHESTRATION_EVENTS)
            self.conn.executescript(CREATE_EVENTS_INDEXES)
            self.conn.execute(CREATE_TAX_SUMMARY)
            self.conn.executescript(CREATE_TAX_INDEXES)
            self.conn.executescript(CREATE_ENERGY_SAMPLES)
            self.conn.executescript(CREATE_CPU_SAMPLES)
            self.conn.executescript(CREATE_INTERRUPT_SAMPLES)
            self.conn.execute(CREATE_ML_VIEW)
        logger.debug("All tables and indexes created (if not existed).")

    def _get_hardware_id(self, hardware_info: Dict[str, Any]) -> Optional[int]:
        """
        Retrieve or insert hardware configuration.

        We match on hostname + kernel version because these are stable identifiers
        for a given machine and OS. If a matching record exists, return its hw_id.
        Otherwise, insert a new record and return the new ID.

        Args:
            hardware_info: Dictionary containing hardware fields (hostname, cpu_model, etc.)

        Returns:
            hw_id (int) if hardware_info provided, else None.
        """
        if not hardware_info:
            return None

        # Try to find existing hardware record.
        cursor = self.conn.execute(
            """
            SELECT hw_id FROM hardware_config
            WHERE hostname = ? AND kernel_version = ?
        """,
            (hardware_info.get("hostname"), hardware_info.get("kernel_version")),
        )
        row = cursor.fetchone()
        if row:
            return row["hw_id"]

        # Insert new hardware record.
        cursor = self.conn.execute(
            """
            INSERT INTO hardware_config
            (hostname, cpu_model, cpu_cores, cpu_threads, ram_gb, kernel_version,
             microcode_version, rapl_domains)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        """,
            (
                hardware_info.get("hostname"),
                hardware_info.get("cpu_model"),
                hardware_info.get("cpu_cores"),
                hardware_info.get("cpu_threads"),
                hardware_info.get("ram_gb"),
                hardware_info.get("kernel_version"),
                hardware_info.get("microcode_version"),
                hardware_info.get("rapl_domains"),
            ),
        )
        return cursor.lastrowid

    def _compute_run_state_hash(
        self, run_data: Dict[str, Any], hw_id: Optional[int], baseline_id: Optional[str]
    ) -> str:
        """
        Compute a cryptographic hash of the system state for reproducibility.

        The hash includes:
        - governor
        - turbo_enabled
        - hw_id (which encodes microcode, kernel, etc.)
        - baseline_id

        This allows us to later group runs that had identical system configuration.

        Args:
            run_data: Dictionary containing run fields (from harness output).
            hw_id: hardware_config primary key (or None).
            baseline_id: idle_baselines primary key (or None).

        Returns:
            SHA‑256 hex digest string.
        """
        # Build a canonical string representation of the state.
        state_str = (
            f"{run_data.get('governor', '')}|"
            f"{run_data.get('turbo_enabled', '')}|"
            f"{hw_id}|"
            f"{baseline_id}"
        )
        return hashlib.sha256(state_str.encode()).hexdigest()

    def save_experiment(
        self,
        experiment_meta: Dict[str, Any],
        runs: List[Dict[str, Any]],
        hardware_info: Optional[Dict[str, Any]] = None,
    ) -> int:
        """
        Save a complete experiment with all its runs.

        This is the main entry point for storing results from the harness.
        It performs the following steps:
        1. Insert the experiment metadata.
        2. If hardware_info provided, insert/get hardware_id.
        3. Insert each run (with all metrics, events, samples).
        4. After all runs are inserted, compute tax summaries by pairing
           linear and agentic runs with the same run_number.

        All inserts happen inside a transaction to ensure atomicity.

        Args:
            experiment_meta: Dictionary with keys:
                - name (str): experiment name
                - description (str): optional description
                - workflow_type (str): 'linear' or 'agentic'
                - model_name (str)
                - provider (str)
                - task_name (str)
                - country_code (str, default 'US')
            runs: List of run result dictionaries from the harness.
                  Each run dict must contain:
                  - 'ml_features' (dict with all the 77 fields)
                  - optionally 'layer3_derived'
                  - optionally 'execution'
                  - optionally 'sustainability'
                  - optionally 'orchestration_events'
                  - optionally 'energy_samples', 'cpu_samples', 'interrupt_samples'
            hardware_info: Optional dict with hardware details.

        Returns:
            exp_id of the newly inserted experiment.
        """
        # ---------------------------------------------------------
        # Step 1: Insert experiment record
        # ---------------------------------------------------------
        with self.conn:
            cursor = self.conn.execute(
                """
                INSERT INTO experiments
                (name, description, workflow_type, model_name, provider, task_name, country_code)
                VALUES (?, ?, ?, ?, ?, ?, ?)
            """,
                (
                    experiment_meta.get("name", "unnamed"),
                    experiment_meta.get("description", ""),
                    experiment_meta.get("workflow_type"),
                    experiment_meta.get("model_name"),
                    experiment_meta.get("provider"),
                    experiment_meta.get("task_name"),
                    experiment_meta.get("country_code", "US"),
                ),
            )
            exp_id = cursor.lastrowid

        # ---------------------------------------------------------
        # Step 2: Get or insert hardware configuration
        # ---------------------------------------------------------
        hw_id = self._get_hardware_id(hardware_info) if hardware_info else None

        # ---------------------------------------------------------
        # Step 3: Insert each run
        # ---------------------------------------------------------
        run_ids = {}  # map experiment_id (string) to database run_id
        for run in runs:
            run_id = self._insert_run(exp_id, hw_id, run)
            # The run's ml_features contains an 'experiment_id' field (UUID from harness)
            # We use that to link later for tax summary.
            exp_uuid = run.get("ml_features", {}).get("experiment_id")
            if exp_uuid:
                run_ids[exp_uuid] = run_id

        # ---------------------------------------------------------
        # Step 4: Create tax summaries for paired runs
        # ---------------------------------------------------------
        self._create_tax_summaries(exp_id, run_ids)


    r.frequency_mhz,
    r.ring_bus_freq_mhz,
    r.package_temp_celsius,
    r.start_temp_c,
    r.max_temp_c,
    r.thermal_delta_c,
    r.c2_time_seconds,
    r.c3_time_seconds,
    r.c6_time_seconds,
    r.c7_time_seconds,
    r.wakeup_latency_us,
    r.interrupt_rate,
    r.thermal_throttle_flag,
    r.rss_memory_mb,
    r.vms_memory_mb,
    r.total_tokens,
    r.prompt_tokens,
    r.completion_tokens,
    r.dns_latency_ms,
    r.api_latency_ms,
    r.compute_time_ms,
    r.governor,
    r.turbo_enabled,

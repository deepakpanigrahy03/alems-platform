CREATE_EXPERIMENTS = """
-- experiments: top‑level metadata for each experiment run.
-- Columns:
--   exp_id            : auto‑increment primary key
--   name              : human‑readable experiment name
--   description       : free‑text description
--   workflow_type     : 'linear' or 'agentic' – defines the execution mode
--   model_name        : LLM model identifier (e.g., 'llama-3.3-70b-versatile')
--   provider          : API provider (e.g., 'groq', 'ollama')
--   task_name         : task identifier (e.g., 'capital', 'explain')
--   country_code      : ISO country code for grid carbon intensity
--   created_at        : timestamp when experiment record was created
CREATE TABLE IF NOT EXISTS experiments (
    exp_id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    description TEXT,
    workflow_type TEXT CHECK(workflow_type IN ('linear','agentic')),
    model_name TEXT,
    provider TEXT,
    task_name TEXT,
    country_code TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
"""

CREATE_HARDWARE_CONFIG = """
-- hardware_config: fixed hardware characteristics of the machine.
-- This table is populated once per machine and reused across runs.
-- Columns:
--   hw_id             : auto‑increment primary key
--   hostname          : machine hostname (for identification)
--   cpu_model         : CPU model string (from /proc/cpuinfo)
--   cpu_cores         : number of physical cores
--   cpu_threads       : number of logical threads (including hyper‑threading)
--   ram_gb            : total RAM in gigabytes (approx)
--   kernel_version    : kernel release (uname -r)
--   microcode_version : CPU microcode version (from /proc/cpuinfo)
--   rapl_domains      : comma‑separated list of available RAPL domains
--   created_at        : timestamp when this record was created
CREATE TABLE IF NOT EXISTS hardware_config (
    hw_id INTEGER PRIMARY KEY AUTOINCREMENT,
    hostname TEXT,
    cpu_model TEXT,
    cpu_cores INTEGER,
    cpu_threads INTEGER,
    ram_gb INTEGER,
    kernel_version TEXT,
    microcode_version TEXT,
    rapl_domains TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
"""

CREATE_IDLE_BASELINES = """
-- idle_baselines: idle power measurements used for baseline correction.
-- Columns:
--   baseline_id        : unique identifier (e.g., 'baseline_1740595200')
--   timestamp          : Unix timestamp (seconds) when baseline was taken
--   package_power_watts: average idle power of the whole package
--   core_power_watts   : average idle power of cores
--   uncore_power_watts : average idle power of uncore (if available)
--   dram_power_watts   : average idle power of DRAM (if available)
--   duration_seconds   : total duration of baseline measurement
--   sample_count       : number of samples taken
--   package_std        : standard deviation of package power (shows stability)
--   core_std           : standard deviation of core power
--   uncore_std         : standard deviation of uncore power
--   dram_std           : standard deviation of DRAM power
--   governor           : CPU governor active during baseline ('performance','powersave')
--   turbo              : 'enabled' or 'disabled' – turbo boost state
--   background_cpu     : average background CPU % during baseline
--   process_count      : number of running processes during baseline
--   method             : how baseline was obtained (e.g., 'fresh_measurement')
CREATE TABLE IF NOT EXISTS idle_baselines (
    baseline_id TEXT PRIMARY KEY,
    timestamp REAL NOT NULL,
    package_power_watts REAL,
    core_power_watts REAL,
    uncore_power_watts REAL,
    dram_power_watts REAL,
    duration_seconds INTEGER,
    sample_count INTEGER,
    package_std REAL,
    core_std REAL,
    uncore_std REAL,
    dram_std REAL,
    governor TEXT,
    turbo TEXT,
    background_cpu REAL,
    process_count INTEGER,
    method TEXT
);
"""

CREATE_RUNS = """
-- runs: core table containing all per‑run aggregated metrics.
-- This is the central table; every other table references it via run_id.
-- Columns are grouped by category for readability.
-- Units are documented in comments after each group.
CREATE TABLE IF NOT EXISTS runs (
    run_id INTEGER PRIMARY KEY AUTOINCREMENT,
    exp_id INTEGER NOT NULL,
    hw_id INTEGER,
    baseline_id TEXT,
    run_number INTEGER,                     -- run index within experiment

    -- Timing (all in nanoseconds)
    start_time_ns INTEGER,
    end_time_ns INTEGER,
    duration_ns INTEGER,

    -- Energy (all in microjoules; convert to Joules by dividing by 1e6)
    total_energy_uj INTEGER,                 -- raw total energy from RAPL
    dynamic_energy_uj INTEGER,                -- total minus baseline energy
    baseline_energy_uj INTEGER,                -- idle energy during same period
    avg_power_watts REAL,                      -- total_energy_j / duration_seconds

    -- Performance counters
    instructions BIGINT,
    cycles BIGINT,
    ipc REAL,                                  -- instructions per cycle
    cache_misses BIGINT,
    cache_references BIGINT,
    cache_miss_rate REAL,                      -- cache_misses / cache_references
    page_faults INTEGER,
    major_page_faults INTEGER,
    minor_page_faults INTEGER,

    -- Scheduler metrics
    context_switches_voluntary INTEGER,
    context_switches_involuntary INTEGER,
    total_context_switches INTEGER,
    thread_migrations INTEGER,
    run_queue_length REAL,                      -- average number of processes waiting
    kernel_time_ms REAL,                         -- time spent in kernel (ms)
    user_time_ms REAL,                           -- time spent in user space (ms)

    -- Frequency & ring bus
    frequency_mhz REAL,                          -- average CPU frequency
    ring_bus_freq_mhz REAL,                      -- ring bus frequency

    -- Thermal metrics
    package_temp_celsius REAL,                    -- average package temperature
    baseline_temp_celsius REAL,                    -- idle temperature from baseline
    start_temp_c REAL,                             -- temperature at start of run
    max_temp_c REAL,                               -- peak temperature during run
    thermal_delta_c REAL,                          -- max_temp - start_temp (heating)
    thermal_during_experiment BOOLEAN,              -- whether thermal throttling occurred during run
    thermal_now_active BOOLEAN,                     -- whether throttling active at end
    thermal_since_boot BOOLEAN,                     -- whether throttling occurred since boot
    experiment_valid BOOLEAN,                       -- true if no throttling during run and at end

    -- C‑state residencies (seconds spent in each state, since boot, but we take delta)
    c2_time_seconds REAL,
    c3_time_seconds REAL,
    c6_time_seconds REAL,
    c7_time_seconds REAL,

    -- MSR / wakeup
    wakeup_latency_us REAL,                         -- average wakeup latency (microseconds)
    interrupt_rate REAL,                             -- interrupts per second during run
    thermal_throttle_flag INTEGER,                   -- raw throttle count from MSR

    -- Memory usage (process level)
    rss_memory_mb REAL,                              -- Resident Set Size (MB)
    vms_memory_mb REAL,                              -- Virtual Memory Size (MB)

    -- Token counts (for LLM calls)
    total_tokens INTEGER,
    prompt_tokens INTEGER,
    completion_tokens INTEGER,

    -- Network latencies (milliseconds)
    dns_latency_ms REAL,
    api_latency_ms REAL,
    compute_time_ms REAL,

    -- System state at run time
    governor TEXT,
    turbo_enabled BOOLEAN,
    is_cold_start BOOLEAN,
    background_cpu_percent REAL,
    process_count INTEGER,

    -- Agentic‑specific metrics (NULL for linear runs)
    planning_time_ms REAL,
    execution_time_ms REAL,
    synthesis_time_ms REAL,
    phase_planning_ratio REAL,
    phase_execution_ratio REAL,
    phase_synthesis_ratio REAL,
    llm_calls INTEGER,
    tool_calls INTEGER,
    tools_used INTEGER,
    steps INTEGER,
    avg_step_time_ms REAL,
    complexity_level INTEGER,
    complexity_score REAL,

    -- Sustainability metrics (from Module 2)
    carbon_g REAL,                                   -- grams CO₂ equivalent
    water_ml REAL,                                   -- milliliters of water
    methane_mg REAL,                                 -- milligrams of methane

    -- Derived efficiency metrics (computed in Python, stored for ML convenience)
    energy_per_instruction REAL,                      -- Joules per instruction
    energy_per_cycle REAL,                            -- Joules per cycle
    energy_per_token REAL,                            -- Joules per token
    instructions_per_token REAL,
    interrupts_per_second REAL,                       -- same as interrupt_rate

    -- Cryptographic run state hash (for reproducibility)
    run_state_hash TEXT,

    FOREIGN KEY(exp_id) REFERENCES experiments(exp_id),
    FOREIGN KEY(hw_id) REFERENCES hardware_config(hw_id),
    FOREIGN KEY(baseline_id) REFERENCES idle_baselines(baseline_id)
);
"""

